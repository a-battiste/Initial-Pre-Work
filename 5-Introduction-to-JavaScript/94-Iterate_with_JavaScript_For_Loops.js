// I learned that a for loop uses three optional expressions which are separated by semicolons. The expressions are: the initialization statement, condition statement, and final expression. The initialization statement executes 1 time before the loop begins. The condition statement tells the loop to continue, as long as the condition remains true. The final expression executes at the end of each loop iteration, and usually increments or decrements the loop counter. These loops run for a specific amount of times. 

// Example
var ourArray = [];

for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}

