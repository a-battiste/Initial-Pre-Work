// I learned how to remove the last item from an array by using the pop function. The pop function can be used when assigning a removed item to a new variable.

// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

